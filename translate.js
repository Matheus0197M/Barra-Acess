function setLanguage(lang) {
    // Salva o idioma selecionado no localStorage para que persista entre as páginas
    localStorage.setItem('selectedLanguage', lang);

    // Itera sobre todos os elementos com o atributo data-translate-key
    document.querySelectorAll('[data-translate-key]').forEach(element => {
        const key = element.getAttribute('data-translate-key');
        if (translations[lang] && translations[lang][key]) {
            // Verifica se é um input/textarea para usar value/placeholder
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else if (element.tagName === 'IMG' && element.hasAttribute('alt')) {
                element.alt = translations[lang][key];
            } else {
                element.innerText = translations[lang][key];
            }
        }
    });

    // Traduz o título da página (tag <title>)
    const pageTitle = document.querySelector('title');
    const originalTitleKey = pageTitle.getAttribute('data-translate-key'); // Pega a chave original do título
    if (pageTitle && originalTitleKey && translations[lang] && translations[lang][originalTitleKey]) {
        pageTitle.innerText = translations[lang][originalTitleKey];
    }
}

// Carrega o idioma salvo ao carregar a página, ou define como 'pt' por padrão
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('selectedLanguage') || 'pt';
    setLanguage(savedLang);

    // Adiciona o evento de clique ao link "Idioma"
    const languageLink = document.getElementById('language-switcher'); // Seleciona pelo ID
    if (languageLink) {
        languageLink.addEventListener('click', (event) => {
            event.preventDefault(); // Evita que a página role para a âncora
            const currentLang = localStorage.getItem('selectedLanguage') || 'pt';
            const newLang = (currentLang === 'pt') ? 'en' : 'pt'; // Alterna entre pt e en
            setLanguage(newLang);
        });
    }
});